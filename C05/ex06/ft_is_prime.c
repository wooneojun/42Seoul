int	ft_is_prime(int nb)
{
	int	i;

	i = 1;
	while (++i < nb)
		if (nb % i == 0)
			break ;
	if (nb <= 1)
		return (0);
	else
	{
		if (i == nb)
			return (1);
		else
			return (0);
	}
}
