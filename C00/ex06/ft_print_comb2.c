#include <unistd.h>

int	print(char n[])
{
	write(1, &"0123456789"[n[0] / 10], 1);
	write(1, &"0123456789"[n[0] % 10], 1);
	write(1, " ", 1);
	write(1, &"0123456789"[n[1] / 10], 1);
	write(1, &"0123456789"[n[1] % 10], 1);
	if (n[0] != 98)
		write(1, ", ", 2);
	return (1);
}

void	ft_print_comb2(void)
{
	char	n[2];

	n[0] = 0;
	while (n[0] <= 98)
	{
		n[1] = n[0] + 1;
		while (n[1] <= 99)
			n[1] += print(n);
		n[0] += 1;
	}
}
