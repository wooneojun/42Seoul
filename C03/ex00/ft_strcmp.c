int	ft_strcmp(char	*s1, char	*s2)
{
	int	i;
	int	flag;

	i = 0;
	flag = 0;
	while (s1[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			flag = s1[i] - s2[i];
			break ;
		}
		i++;
	}
	if (flag == 0 && s2[i] != '\0')
		flag = -s2[i];
	return (flag);
}
